import React from 'react'

export default function ProductCard({ product, onAdd, onOpen }) {
  const formatPrice = (price, currency = 'CLP') => {
    const formatted = Number(price).toLocaleString('es-CL')
    return currency === 'CLP' ? `$${formatted}` : `${formatted} ${currency}`
  }

  return (
    <div className="col">
      <div className="card h-100 shadow-sm">
        <div style={{ position: 'relative' }}>
          <img 
            src={product.image} 
            className="card-img-top" 
            alt={product.name} 
            style={{ objectFit: 'cover', height: 200, cursor: 'pointer' }} 
            onClick={() => onOpen(product)}
          />
          {/* Badge indicador de múltiples imágenes */}
          {product.totalImages > 1 && (
            <div 
              style={{
                position: 'absolute',
                top: '10px',
                right: '10px',
                backgroundColor: 'rgba(0, 0, 0, 0.7)',
                color: 'white',
                padding: '4px 8px',
                borderRadius: '12px',
                fontSize: '12px',
                fontWeight: 'bold',
                display: 'flex',
                alignItems: 'center',
                gap: '4px'
              }}
            >
              <i className="fas fa-images"></i>
              {product.totalImages}
            </div>
          )}
        </div>
        <div className="card-body d-flex flex-column">
          <h6 className="card-title">{product.name}</h6>
          <p className="card-text text-muted small mb-2">
            <i className="fas fa-tag me-1"></i>
            {product.category}
          </p>
          {product.description && (
            <p className="card-text small text-secondary mb-2" style={{ 
              overflow: 'hidden', 
              textOverflow: 'ellipsis', 
              display: '-webkit-box', 
              WebkitLineClamp: 2, 
              WebkitBoxOrient: 'vertical' 
            }}>
              {product.description}
            </p>
          )}
          <div className="mt-auto d-flex justify-content-between align-items-center">
            <div className="fw-bold text-success fs-5">
              {formatPrice(product.price, product.currency)}
            </div>
            <div>
              <button 
                className="btn btn-sm btn-outline-primary me-2" 
                onClick={() => onOpen(product)}
                title="Ver detalles"
              >
                <i className="fas fa-eye"></i>
              </button>
              <button 
                className="btn btn-sm btn-success" 
                onClick={() => onAdd(product)}
                title="Agregar al carrito"
              >
                <i className="fas fa-cart-plus me-1"></i>
                Agregar
              </button>
            </div>
          </div>
        </div>
      </div>
    </div>
  )
}
